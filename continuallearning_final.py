# Cell 10: Training + evaluation helpers

def train_one_epoch(model, loader, optimizer, criterion, task_id):
    model.train()
    total_loss, correct, total = 0, 0, 0
    for imgs, labels in loader:
        imgs, labels = imgs.to(device), labels.to(device)
        optimizer.zero_grad()
        outputs = model(imgs, task_id=task_id)
        loss    = criterion(outputs, labels)
        loss.backward(); optimizer.step()
        total_loss += loss.item() * imgs.size(0)
        correct    += (outputs.argmax(1) == labels).sum().item()
        total      += imgs.size(0)
    return total_loss / total, correct / total

@torch.no_grad()
def evaluate(model, loader, task_id):
    model.eval()
    correct, total = 0, 0
    for imgs, labels in loader:
        imgs, labels = imgs.to(device), labels.to(device)
        outputs = model(imgs, task_id=task_id)
        correct += (outputs.argmax(1) == labels).sum().item()
        total   += imgs.size(0)
    return correct / total

def evaluate_all_tasks(model, task_loaders, up_to_task):
    task_keys = list(task_loaders.keys())
    results   = {}
    for i in range(up_to_task + 1):
        key = task_keys[i]
        results[key] = evaluate(model, task_loaders[key]["val"], task_id=i)
    return results

