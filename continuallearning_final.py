def get_targets(dataset):
    if hasattr(dataset, 'targets'):
        return np.array(dataset.targets)
    elif hasattr(dataset, '_labels'):
        return np.array(dataset._labels)
    elif hasattr(dataset, 'samples'):
        return np.array([s[1] for s in dataset.samples])
    else:
        raise AttributeError(f"Cannot find labels in {type(dataset)}")

def make_binary_loader(dataset, class_a_idx, class_b_idx,
                       val_split=0.2, batch_size=64):
    targets   = get_targets(dataset)
    idx_a     = np.where(targets == class_a_idx)[0].tolist()
    idx_b     = np.where(targets == class_b_idx)[0].tolist()
    all_idx   = idx_a + idx_b
    label_map = {class_a_idx: 0, class_b_idx: 1}

    class BinarySubset(torch.utils.data.Dataset):
        def __init__(self, base, indices, lmap):
            self.base    = base
            self.indices = indices
            self.lmap    = lmap
        def __len__(self):
            return len(self.indices)
        def __getitem__(self, i):
            img, lbl = self.base[self.indices[i]]
            return img, self.lmap[lbl]

    full    = BinarySubset(dataset, all_idx, label_map)
    n_val   = int(len(full) * val_split)
    n_train = len(full) - n_val
    train_set, val_set = random_split(
        full, [n_train, n_val],
        generator=torch.Generator().manual_seed(42)
    )
    train_loader = DataLoader(train_set, batch_size=batch_size,
                              shuffle=True,  num_workers=2, pin_memory=True)
    val_loader   = DataLoader(val_set,   batch_size=batch_size,
                              shuffle=False, num_workers=2, pin_memory=True)
    return train_loader, val_loader


