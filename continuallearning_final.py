# Cell 13: EWC — Elastic Weight Consolidation

class EWC:
    def __init__(self, lambda_ewc=5000):
        self.lambda_ewc = lambda_ewc
        self.fisher     = {}
        self.optima     = {}

    def update(self, model, loader, task_id, n_samples=200):
        model.eval()
        fisher_tmp = {n: torch.zeros_like(p) for n, p in model.named_parameters() if p.requires_grad}
        count = 0
        for imgs, labels in loader:
            if count >= n_samples: break
            imgs, labels = imgs.to(device), labels.to(device)
            model.zero_grad()
            loss = nn.CrossEntropyLoss()(model(imgs, task_id=task_id), labels)
            loss.backward()
            for n, p in model.named_parameters():
                if p.requires_grad and p.grad is not None:
                    fisher_tmp[n] += p.grad.detach() ** 2
            count += imgs.size(0)
        for n in fisher_tmp: fisher_tmp[n] /= count
        for n in fisher_tmp:
            self.fisher[n] = self.fisher.get(n, torch.zeros_like(fisher_tmp[n])) + fisher_tmp[n]
        for n, p in model.named_parameters():
            if p.requires_grad: self.optima[n] = p.detach().clone()
        print(f"  EWC updated after Task {task_id+1} ({count} samples, lambda={self.lambda_ewc})")

    def penalty(self, model):
        if not self.fisher: return torch.tensor(0.0, device=device)
        loss = torch.tensor(0.0, device=device)
        for n, p in model.named_parameters():
            if p.requires_grad and n in self.fisher:
                loss += (self.fisher[n] * (p - self.optima[n]) ** 2).sum()
        return (self.lambda_ewc / 2) * loss

print("EWC class defined.")

# Cell 14: EWC training loop

def train_one_epoch_ewc(model, loader, optimizer, criterion, task_id, ewc):
    model.train()
    total_loss, ce_loss_total, ewc_loss_total = 0, 0, 0
    correct, total = 0, 0
    for imgs, labels in loader:
        imgs, labels = imgs.to(device), labels.to(device)
        optimizer.zero_grad()
        outputs  = model(imgs, task_id=task_id)
        ce_loss  = criterion(outputs, labels)
        ewc_loss = ewc.penalty(model)
        loss     = ce_loss + ewc_loss
        loss.backward(); optimizer.step()
        total_loss     += loss.item()     * imgs.size(0)
        ce_loss_total  += ce_loss.item()  * imgs.size(0)
        ewc_loss_total += ewc_loss.item() * imgs.size(0)
        correct        += (outputs.argmax(1) == labels).sum().item()
        total          += imgs.size(0)
    return total_loss/total, ce_loss_total/total, ewc_loss_total/total, correct/total

