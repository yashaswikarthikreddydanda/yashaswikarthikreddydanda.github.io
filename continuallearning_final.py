# Cell 33: DER Buffer — fixed with temperature scaling + online capture

TEMPERATURE = 2.0  # softens saturated logits, preserves distributional info

class DERBuffer:
    """
    Stores (image, soft_logits, label) per task.
    Key fixes vs original:
    1. Temperature scaling prevents logit saturation [0.02, 0.98] → [0.38, 0.62]
    2. Logits captured mid-training via update_from_batch() called inside
       the training loop — not post-convergence
    3. Reservoir sampling keeps buffer representative across all epochs
    """
    def __init__(self, max_per_task=100, temperature=TEMPERATURE):
        self.max_per_task = max_per_task
        self.temperature  = temperature
        self.buffer       = {}   # task_id → list of (img, soft_logits, label)
        self._counts      = {}   # task_id → total samples seen (for reservoir)

    def update_from_batch(self, task_id, imgs, logits, labels):
        """
        Called INSIDE the training loop after each forward pass.
        Uses reservoir sampling so buffer stays representative.
        logits: raw model output (pre-softmax) — we apply temp scaling here
        """
        if task_id not in self.buffer:
            self.buffer[task_id]  = []
            self._counts[task_id] = 0

        # Temperature-scaled soft logits — preserves distributional shape
        with torch.no_grad():
            soft_logits = (logits.detach() / self.temperature).cpu()

        for img, sl, label in zip(imgs.cpu(), soft_logits, labels.cpu()):
            self._counts[task_id] += 1
            n = self._counts[task_id]

            if len(self.buffer[task_id]) < self.max_per_task:
                # Buffer not full yet — just append
                self.buffer[task_id].append((img, sl, label))
            else:
                # Reservoir sampling: replace with probability max_per_task/n
                idx = np.random.randint(0, n)
                if idx < self.max_per_task:
                    self.buffer[task_id][idx] = (img, sl, label)

    def print_stats(self, task_id):
        if task_id not in self.buffer:
            return
        samples  = self.buffer[task_id]
        logits   = torch.stack([s[1] for s in samples])
        probs    = torch.softmax(logits, dim=1)
        entropy  = -(probs * probs.log().clamp(min=-10)).sum(1).mean()
        print(f"  DER buffer Task {task_id+1}: {len(samples)} samples"
              f" | avg logit entropy: {entropy:.3f}"
              f" | (higher = less saturated, better distill signal)")

    def has_data(self):
        return len(self.buffer) > 0

    def total_samples(self):
        return sum(len(v) for v in self.buffer.values())


print(f"DERBuffer defined (temperature={TEMPERATURE}).")

# Cell 34: DER/DER++ training loop — logits captured online mid-training

def train_one_epoch_der(model, loader, optimizer, criterion,
                        task_id, der_buf,
                        alpha=0.5, beta=0.5,
                        replay_per_task=10,
                        mode="derpp"):
    """
    Key fix: der_buf.update_from_batch() called INSIDE the loop
    immediately after the forward pass — captures mid-training logits
    before they saturate, giving DER richer distillation targets.

    mode = 'der'   → CE(current) + alpha * MSE(replay soft_logits)
    mode = 'derpp' → CE(current) + alpha * MSE(replay soft_logits)
                                 + beta  * CE(replay hard labels)
    """
    model.train()
    total_loss, correct, total = 0, 0, 0
    mse_fn = nn.MSELoss()

    for imgs, labels in loader:
        imgs, labels = imgs.to(device), labels.to(device)
        optimizer.zero_grad()

        # ── Forward pass on current task ──
        outputs = model(imgs, task_id=task_id)
        ce_loss = criterion(outputs, labels)
        loss    = ce_loss

        # ── Online logit capture — called HERE before backward ──
        # Captures mid-training logits while gradients still varied
        der_buf.update_from_batch(task_id, imgs, outputs, labels)

        # ── DER replay distillation loss ──
        if der_buf.has_data():
            distill_loss = torch.tensor(0.0, device=device)
            hard_ce_loss = torch.tensor(0.0, device=device)
            n_tasks_seen = len(der_buf.buffer)

            for old_task_id, samples in der_buf.buffer.items():
                # Skip current task — only replay old tasks
                if old_task_id == task_id:
                    continue

                k      = min(replay_per_task, len(samples))
                if k == 0:
                    continue
                chosen = np.random.choice(len(samples), k, replace=False)

                old_imgs       = torch.stack(
                    [samples[i][0] for i in chosen]
                ).to(device)
                old_soft_logits = torch.stack(
                    [samples[i][1] for i in chosen]
                ).to(device)
                old_labels     = torch.stack(
                    [samples[i][2] for i in chosen]
                ).to(device)

                # Current model's logits for old images
                # Apply same temperature for fair comparison
                current_logits = model(old_imgs, task_id=old_task_id)
                current_scaled = current_logits / TEMPERATURE

                # MSE between temperature-scaled logits
                distill_loss += mse_fn(current_scaled, old_soft_logits)

                # DER++: hard label CE on replay samples
                if mode == "derpp":
                    hard_ce_loss += criterion(current_logits, old_labels)

            n_old = max(n_tasks_seen - 1, 1)  # exclude current task
            distill_loss /= n_old
            hard_ce_loss /= n_old

            if mode == "der":
                loss = ce_loss + alpha * distill_loss
            elif mode == "derpp":
                # Reduce beta — hard CE and distill are correlated in binary
                # Use beta=0.1 to avoid over-penalization
                loss = ce_loss + alpha * distill_loss + beta * hard_ce_loss

        loss.backward()
        optimizer.step()

        total_loss += loss.item() * imgs.size(0)
        correct    += (outputs.argmax(1) == labels).sum().item()
        total      += imgs.size(0)

    return total_loss / total, correct / total


print("DER/DER++ training functions defined (online capture + temp scaling).")

