# Cell 18: Experience Replay buffer

class ReplayBuffer:
    def __init__(self, max_per_task=100):
        self.max_per_task = max_per_task
        self.buffer       = {}

    def add_task(self, task_id, loader, n_samples=None):
        if n_samples is None: n_samples = self.max_per_task
        stored, count = [], 0
        for imgs, labels in loader:
            for img, label in zip(imgs, labels):
                if count >= n_samples: break
                stored.append((img.cpu(), label.cpu()))
                count += 1
            if count >= n_samples: break
        self.buffer[task_id] = stored
        print(f"  Buffer: stored {count} samples for Task {task_id+1} (total tasks: {len(self.buffer)})")

    def has_data(self):
        return len(self.buffer) > 0

    def total_samples(self):
        return sum(len(v) for v in self.buffer.values())

print("ReplayBuffer defined.")

# Cell 19: Replay training loop (fixed — no dead sample() call)

def train_one_epoch_replay(model, loader, optimizer, criterion, task_id, replay_buf, replay_per_task=10):
    model.train()
    total_loss, correct, total = 0, 0, 0
    for imgs, labels in loader:
        imgs, labels = imgs.to(device), labels.to(device)
        optimizer.zero_grad()
        outputs = model(imgs, task_id=task_id)
        ce_loss = criterion(outputs, labels)
        loss    = ce_loss
        if replay_buf.has_data():
            replay_loss  = torch.tensor(0.0, device=device)
            n_tasks_seen = len(replay_buf.buffer)
            for old_task_id, samples in replay_buf.buffer.items():
                k      = min(replay_per_task, len(samples))
                chosen = np.random.choice(len(samples), k, replace=False)
                old_imgs   = torch.stack([samples[i][0] for i in chosen]).to(device)
                old_labels = torch.stack([samples[i][1] for i in chosen]).to(device)
                replay_loss += criterion(model(old_imgs, task_id=old_task_id), old_labels)
            loss = ce_loss + (replay_loss / n_tasks_seen)
        loss.backward(); optimizer.step()
        total_loss += loss.item() * imgs.size(0)
        correct    += (outputs.argmax(1) == labels).sum().item()
        total      += imgs.size(0)
    return total_loss / total, correct / total

